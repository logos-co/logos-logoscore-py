"""Integration tests calling every method on `test_basic_module_cpp`.

Mirror of `test_basic_module_methods.py` but for the pure-C++ universal
module — the impl signatures use `std::string`, `int64_t`, `uint64_t`,
`double`, `std::vector<std::string>`, `std::vector<uint8_t>`, `LogosMap`
(nlohmann::json), `LogosList`, and `StdLogosResult`. The Qt glue is
auto-generated by `logos-cpp-generator --from-header`, and this file
proves the generated glue + over-the-wire serialisation works end-to-end
for every type the generator supports.

Return-value shapes are kept identical to `test_basic_module` wherever
the mapping is lossless:

  std::string              ↔ QString                    (same shape)
  int64_t / uint64_t       ↔ int                        (same shape)
  bool                     ↔ bool                       (same shape)
  double                   ↔ double                     (same shape)
  std::vector<std::string> ↔ QStringList                (JSON array of strings)
  std::vector<uint8_t>     ↔ QByteArray                 (→ base64 or bytes; tested via size)
  LogosMap                 ↔ QVariantMap (jsonReturn)   (JSON object)
  LogosList                ↔ QVariantList (jsonReturn)  (JSON array)
  StdLogosResult           ↔ LogosResult                ({success, value, error})

See:
  repos/logos-test-modules/test-basic-module-cpp/src/test_basic_module_cpp_impl.h
  repos/logos-test-modules/test-basic-module-cpp/src/test_basic_module_cpp_impl.cpp

Skipped unless LOGOSCORE_BIN and LOGOSCORE_TEST_MODULES_DIR are set — the
Nix `integration` check wires both up (and bundles this module into the
test modules dir alongside `test_basic_module`).
"""
from __future__ import annotations

import threading
import time

import pytest

from logoscore import LogoscoreDaemon

MODULE = "test_basic_module_cpp"


@pytest.fixture(scope="module")
def client(logoscore_bin, test_modules_dir, transport, request):
    """Build a daemon + client wired to whatever transport the suite is
    parametrised on. Mirror of `test_basic_module_methods.py::client`
    — kept duplicated rather than moved to a conftest helper so each
    test file can be read end-to-end without jumping between files."""
    import socket

    def _pick_free_port() -> int:
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
            s.bind(("127.0.0.1", 0))
            return s.getsockname()[1]

    kwargs = {}
    client_kwargs: dict = {"transport": transport}
    if transport != "local":
        kwargs["transports"] = [transport]
        if transport == "tcp":
            kwargs["tcp_port"] = _pick_free_port()
        elif transport == "tcp_ssl":
            cert, key = request.getfixturevalue("self_signed_cert")
            kwargs["tcp_ssl_port"] = _pick_free_port()
            kwargs["ssl_cert"] = cert
            kwargs["ssl_key"] = key
            # Self-signed cert won't validate against any CA — the
            # client has to opt out of peer verification or every call
            # fails the TLS handshake.
            client_kwargs["no_verify_peer"] = True
    with LogoscoreDaemon(
        modules_dir=test_modules_dir, binary=logoscore_bin, **kwargs,
    ) as daemon:
        c = daemon.client(**client_kwargs)
        c.load_module(MODULE)
        yield c


