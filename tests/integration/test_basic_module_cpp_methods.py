# ── Return type: StdLogosResult ──────────────────────────────────────────────
# Serialised the same shape as LogosResult (Qt) on the wire:
#   {"success": bool, "value": <any>, "error": <any>}
# Generator emits a StdLogosResult → Qt LogosResult conversion in the glue.
#
# Quirk: on success the impl sets `error=""`, but the glue → QVariant
# conversion treats an empty std::string as a default-constructed QVariant,
# which serialises as JSON `null` rather than an empty string. Same goes
# for `value` on failure (default-constructed nlohmann::json → QVariant()
# → null). So absent-side fields are `None` regardless of their C++ type.

