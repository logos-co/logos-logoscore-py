# ── Events ──────────────────────────────────────────────────────────────────
# Pure-C++ impls emit events through a `std::function emitEvent` member
# that the generator auto-wires to `LogosProviderBase::emitEvent`. The
# generator's signature is `(name, data)` with `data` as a single string,
# so for multi-field events the impl packs the payload as JSON.

def test_emit_test_event(client):
    received: list[dict] = []
    evt = threading.Event()

    def on_event(event: dict) -> None:
        received.append(event)
        evt.set()

    with client.on_event(MODULE, "testEvent", on_event):
        time.sleep(0.5)  # let the watcher subscribe before emitting
        client.call(MODULE, "emitTestEvent", "hello-cpp")
        assert evt.wait(timeout=10.0), "testEvent not received"

    assert received, "expected at least one event"
    assert "hello-cpp" in str(received[0])


def test_emit_multi_arg_event(client):
    """Impl packs (name, count) as a JSON string payload. Assert both
    fragments round-trip."""
    received: list[dict] = []
    evt = threading.Event()

    def on_event(event: dict) -> None:
        received.append(event)
        evt.set()

    with client.on_event(MODULE, "multiArgEvent", on_event):
        time.sleep(0.5)
        client.call(MODULE, "emitMultiArgEvent", "cpp-label", 77)
        assert evt.wait(timeout=10.0), "multiArgEvent not received"

    flat = str(received[0])
    assert "cpp-label" in flat
    assert "77" in flat
